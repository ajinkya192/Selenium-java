package control_statement;

public class Switch_statement {

	public static void main(String[] args) {
		
		int a=50;
		
		switch (a) {
		case 10:
			System.out.println("10");
			
		case 20:
			System.out.println("20");
			
		case 30:
			System.out.println("30");
			
		default:
			System.out.println("number not in 10 20 & 30");
		}

	}

}
